package hk.ust.comp3111.engine;

/**
 * The enum representing the status of a game.
 */
public enum GameStatus {
    /**
     * The game is ongoing.
     */
    ONGOING,
    /**
     * Tom (the computer) wins the game.
     */
    TOM_WINS,
    /**
     * Jerry (the user) wins the game.
     */
    JERRY_WINS
}
