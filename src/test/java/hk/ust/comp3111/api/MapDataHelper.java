    public static MapData getMap() {
        return getMap(input);
    }

    public static MapData getMap(String input) {
        List<List<Vertex>> vertices = new ArrayList<>();
        List<List<Vertex.TileType>> types = Arrays.stream(input.split("\n"))
                .map(s -> s.endsWith(",") ? s.substring(0, s.length() - 1) : s)
                .map(s -> List.of(s.split(",")))
                .map(s -> s.stream().map(String::trim)
                        .map(Integer::parseInt)
                        .map(Vertex.TileType::from).collect(Collectors.toList()))
                .toList();
        for (int i = 0; i < MapData.DEFAULT_HEIGHT; i++) {
            List<Vertex> vertex = new ArrayList<>();
            for (int j = 0; j < MapData.DEFAULT_WIDTH; j++) {
                vertex.add(new Vertex(new Coord(j, i), types.get(i).get(j)));
            }
            vertices.add(vertex);
        }
        return new MapData(vertices);
    }

    public static void display(MapData data) {
        MapComponent comp = new MapComponent(data);
        JFrame frame = new JFrame();
        frame.add(comp.getMainPanel());
        frame.setVisible(true);
    }
